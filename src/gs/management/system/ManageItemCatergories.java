        if (catname.getText().isEmpty() || catdescription.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Make sure Category Name and Description is entered");
        } 
        else {
            String name,des;

            name = this.catname.getText();
            des = this.catdescription.getText();


            controller.addNewCategory(name,des);
            JOptionPane.showMessageDialog(this, "Category Added Succesfully");
            display();
    }          
