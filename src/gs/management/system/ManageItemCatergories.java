    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        if (catid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Category ID which one you want to remove");
        } else {
            String text = catid.getText();
            int id = Integer.parseInt(text);

            controller.removeCategory(id);
            JOptionPane.showMessageDialog(this, "Category removed successfully!!!");
            display();
        }
    }//GEN-LAST:event_deletebtnActionPerformed

    public void display()
    {
      try {
           cattable.setModel(new DefaultTableModel(null,new String[]{"Category Id","Category Name","Catgory Description"}));
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gs_store?serverTimezone=UTC", "root", "");
            Statement st = con.createStatement();
            String sql = "select * from categorytable";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String id = String.valueOf(rs.getInt("catid"));
                String name = rs.getString("catname");
                String des = rs.getString("catdescription");
                
                String tdata[] = {id, name,des};
                DefaultTableModel tblModel = (DefaultTableModel) cattable.getModel();
                tblModel.addRow(tdata);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
